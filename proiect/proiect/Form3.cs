    public partial class Form3 : Form
    {
        public Form3()
        {
            InitializeComponent();
        }

        private void button1_Click(object sender, EventArgs e)
        {
            int nr = Convert.ToInt32(txtIntrare.Text);
            int p = nr * 2;
            int g = p * 2;
            txtIesire.Text = Convert.ToString(nr + p + g);
        }

        private void Cifre_KeyPress(object sender, KeyPressEventArgs e)
        {
            if (char.IsDigit(e.KeyChar) == false && char.IsControl(e.KeyChar) == false)
            {
                e.Handled = true;
            }
        }
    }
